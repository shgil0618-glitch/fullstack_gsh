package com.company.java006_ex;

public class ArrayEx08 {
   public static void main(String[] args) {
      
       String[] name={"아이언맨","헐크","캡틴","토르","호크아이"};
       int[] kor={100,20,90,70,35};   
       int[] eng={100,50,95,80,100};
       int[] mat={100,30,90,60,100};
       int[] aver=new int[5];
      int total[] = new int[5];
      String pass[] = new String[5];
      String mvp[] = new String[5];
      
      System.out.println(":::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::");
       System.out.println("이름\t\t국어\t영어\t수학\t평균\t합격여부\t장학생");
       System.out.println(":::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::");
       for(int i=0;i<name.length;i++) {
          total[i] += kor[i]+eng[i]+mat[i];
          aver[i] = total[i]/3;
          
          if(aver[i]>=60) {pass[i]="합격";}
          else {pass[i]="불합격";}
          
          if(aver[i]>=95) {mvp[i]="장학생";}
          else {mvp[i]="----";}
          
          System.out.printf("%s\t\t%d\t%d\t%d\t%d\t%s\t%s",name[i],kor[i],eng[i],mat[i],aver[i],pass[i],mvp[i]);
          System.out.println();
       }
       System.out.println(":::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::");
   }
}

